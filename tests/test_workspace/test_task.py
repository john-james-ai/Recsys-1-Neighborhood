import inspect
from datetime import datetime
import pytest
import logging

from recsys.workflow.task import Task
from tests.test_workspace.test_operator import TestOperator


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.task
class TestTask:  # pragma: no cover
    # ============================================================================================ #
    def test_tesk(self, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        NAME = "test_task"
        DESCRIPTION = "Test Task Description"
        OPERATOR = TestOperator()
        task = Task(name=NAME, description=DESCRIPTION, operator=OPERATOR)
        task.run()
        assert task.name == NAME
        assert task.description == DESCRIPTION
        assert isinstance(task.started, datetime)
        assert isinstance(task.ended, datetime)
        assert isinstance(task.duration, float)
        assert isinstance(task.operator, TestOperator)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)

