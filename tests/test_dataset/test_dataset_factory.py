import inspect
from datetime import datetime
import pytest
import logging

from recsys.dataset.factory import DatasetFactoryOperator
from recsys.dataset.movielens import MovieLens


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.dataset
class TestDatasetFactoryOperator:  # pragma: no cover
    # ============================================================================================ #
    def test_factory(self, dataframe, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        name = "test_dataset_from_factory"
        desc = "Test Dataset from DatasetFactoryOperator"
        dataset_type = "movielens"  # Case incensitive
        factory = DatasetFactoryOperator(name=name, desc=desc, dataset_type=dataset_type)
        dataset = factory(data=dataframe)
        assert isinstance(dataset, MovieLens)
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
